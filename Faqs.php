                        <h5>Q1. What PTS is offering?</h5>
                        Ans: PTS offers entry test of all types of institutions resides recruitment test for public and private sector institutions within country and across the world. PTS conducts aptitude tests, entry level examinations and selection process based on merit for public and private sector institutions ensuring fairness and transparency through quality based assessment.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q2. Am I eligible for an admission or recruitment?</h5>
                        Ans:	If you are interested in applying for entry test to gain admission in an academic institution it is recommended to go through ToRs of institution for which you are applying. In case of admissions in foreign institutions, it is necessary to go through their pre-requisites for admissions generally available at their website. In case of appointment, it is necessary to go through the details and ToRs of the job duly advertised in Print and Electronic Media.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q3. From Where Can I Get the Information About the PTS Tests?</h5>
                        Ans:	Information about PTS tests can be acquired from any of the following sources:
                        <br>
                        a.	Advertisements through Print Media,
                        <br>
                        b.	Advertisements through Electronic Media and
                        <br>
                        c.	PTS website (www.pts.org.pk)
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q4. From Where Can I Get the Application Forms?</h5>
                        Ans:	You can download Application Forms, free of charge, from PTS website (http://www.pts.org.pk) before the last date for registration for the relevant admission/recruitment.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q5. How Can I Register for PTS Test?</h5>
                        Ans:	Please fill your downloaded application form according to the instructions provided. Submit it to PTS given address after online depositing the Test Fee in any branch of UBL, ABL, MCB or HBL.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q6. What is required for filling â€“up a form?</h5>
                        Ans:	Any incomplete, inappropriate or unclear information, provided in the form, may cause delay in the registration process and you may not be able to take the test on time. Some general instructions to complete the form are as follows:
                        <br>
                        a.	The application forms which can be typed, fill application form and print it. In case of hard copy, use black ink and BLOCK LETTERS shall be used to fill out the form.
                        <br>
                        b.	Download the appropriate test application form as per prescribed requirements.
                        <br>
                        c.	Exercise due care in the spellings of your name, father name, caste, address and CNIC number for future correspondence with PTS. Write the correspondence/ mailing address accurately to avoid any inconvenience.
                        <br>
                        d.	Test Fee is submitted in the form of Online Deposit Slip in any nearest UBL, ABL, MCB or HBL branch, PTS Copy of the deposit challan (Original) must be attached with the Application Form.
                        <br>
                        e.	Also indicate your choice for the city, where you would prefer to take the test out of the options provided.
                        <br>
                        <br>
                        Please ensure:
                        <br>
                        (i)	Two passport size photographs are affixed at appropriate places on form. (Use gum instead of staples)
                        <br>
                        (ii)	Original Bank Deposit Slip (PTS Copy),
                        <br>
                        (iii)	CNIC (Photo Copy)
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q7. Where shall I Submit the Form?</h5>
                        Ans:	After you fill the form according to the instructions, send your form through any courier service or mail (Post office).
                        <br>
                        Make sure to attach:
                        <br>
                        i)	Two copies of your recent photographs,
                        <br>
                        ii)	Clearly written mobile number.
                        <br>
                        iii)	If any essential requirement is missing in the Registration Form, it will be clearly marked as rejected.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q8. How do I confirm that I have been registered?</h5>
                        After submitting the Application Form you will receive your Roll Number Slip (Call Letter) containing Roll No,
                        Test Venue and other details on your mailing address. This slip confirms your registration. You can also visit
                        PTS website for confirmation of the same. You must bring the slip with you to the test center on the day of
                        your test otherwise you shall not be allowed to sit in the Examination Hall.
                        In case of any query regarding the registration process you can contact PTS offices.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q9. Can I Register on the Day of Examination?</h5>
                        Ans:No. The registration of candidates on the day of PTS Examination cannot be entertained. The candidate must get registered before the deadline, announced by PTS, for registration.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q10. How Can I Get Help in Case of Any Problem During Registration Process?</h5>
                        Ans:	If you have any query about the registration process you can clarify by calling the PTS Office:- http://pts.org.pk/contact-us.aspx
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q11. How Can I Get the Test Schedule?</h5>
                        Ans: If you have any query about the Test Schedule you can get the details from the news media and also from PTS website i.e. http://www.pts.org.pk
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q12. What is a Roll number/Identification slip?</h5>
                        Ans: Roll Number Slip confirms your registration. It serves as your Identification Slip and also informs you about the location of the center.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q13. How to Find the Place and Schedule of Test?</h5>
                        Ans: The schedule, time, location, etc., is published through the news media and PTS website i.e. www.PTS.org.pk from where you can get further help and other information. Besides, the Roll number slip contains the desired information also.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q14. How to Get Myself Identified at the Test Center?</h5>
                        Ans: The Roll number slip acts as your identification slip at the test centre. The staff at the Test Center identifies you with the photograph attached on the slip or from your CNIC. If you fail to show your Roll No. slip to the administration on the test day or if your photograph does not match your appearance, you may not be allowed to enter the Test Center. It is advised to keep your CNIC with you while appearing for the test.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q15. What Should I Take With Me to the Test Center?</h5>
                        Ans: You should take sharpened pencils, Ink-pen or Ball point with eraser and clip-board. Besides this the candidate is also asked to show his/her Roll No. Slip.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q16. What should I not take with me to the Test Center?</h5>
                        Ans: Calculators, cell phones, books, booklets, any kind of paper or cheating material are strictly not allowed to be taken inside the test center. If you are found with any of these items with you during the test, you shall be disqualified.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q17. Where shall I sit in my examination test?</h5>
                        Ans: Enter the examination hall and look for the seat that has your roll number pasted on it. The seats are arranged in a sequential order of roll numbers. If you do not find your seat, please do not hesitate to ask any of the invigilators at the center.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q18. What are the Rules and Regulations applicable inside the Test Center?</h5>
                        Ans: Following are the Rules and Regulations that are applied inside the center and which are binding on all candidates:

                        <br>
                        i)	The test is given on the day and at the time scheduled. You are assigned a seat by the invigilation staff or the PTS person. Late comers may not be allowed to sit in the examination.
                        <br>
                        ii)	Testing aids are not permitted at Test Center. Items like calculators, pagers, cell phones, headphones, any paper or writing material, dictionaries etc., are prohibited in the test center.
                        <br>
                        iii)	You are not allowed to eat, drink or use tobacco during the test.
                        <br>
                        iv)	If you do not show the Roll No. Slip and your photograph does not match, you will not be permitted to take the test.
                        <br>
                        v)	No discussion or communication with the fellow candidates is allowed during the test session. Any communication of such type may lead to disqualification.
                        <br>
                        vi)	You are also not allowed to leave Test Center without the permission of the supervisor.
                        <br>
                        vii)	Under no circumstances the test questions or any part of a test be removed, reproduced or disclosed, electronically, or physically or by any other means to any person or entity.
                        <br>
                        viii)	Test centers do not have large waiting areas. Friends or relatives who accompany the candidates to the test center are not permitted to wait in the near vicinity of test center or contact them during the test.
                        <br>
                        ix)	If you need to leave your seat at any time during the test, raise your hand and seek permission of the invigilator. No additional time shall be provided for the same.
                        <br>
                        x)	PTS reserves the right to videotape any or all of the testing sessions.
                        <br>
                        xi)	Dress in such a way that you can adapt to any room temperature.
                        <br>
                        xii)	You are not permitted to leave the test center vicinity during the test session or breaks.
                        <br>
                        xiii)If at any time during the test you believe that you have a problem with your test, or need the Invigilation Staff for any reason, raise your hand to notify the Invigilation Staff.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q19. On What Basis Can I be disqualified from the Test?</h5>
                        Ans: A candidate can be disqualified on following grounds:-
                        <br>
                        i)	Arriving late at the test center.
                        <br>
                        ii)	Taking the test for someone else.
                        <br>
                        iii)	Failing to provide the Roll No. Slip.
                        <br>
                        iv)	Using any objectionable material including testing aids during the test.
                        <br>
                        v)	Trying to create disturbance inside the test center.
                        <br>
                        vi)	Showing ill behavior towards the invigilation staff.
                        <br>
                        vii)	Trying to communicate with the fellow candidates while the test is in progress.
                        <br>
                        viii)	Trying to tear pages from the question paper.
                        <br>
                        ix)	Showing indiscipline i.e. eating, drinking and smoking etc.
                        <br>
                        x)	Leaving the test center without permission.
                        <br>
                        xi)	Violation any of the regulations, applicable at the test centre the decision to disqualify a candidate reserves solely with the invigilators / PTS management.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q20. How shall I take test?</h5>
                        Ans: The invigilators hands over the question book and answer sheets, read the directions carefully. Fill in the essential information in the blank columns of answer sheet. While attempting to answer questions, read the choices in each question and mark the corresponding space on the answer sheet. Do keep the time aspect in mind no extra time will be provided in any case.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q21. What should I write on?</h5>
                        Ans: Write Your Name, Father's Name, Roll Number etc (if required) on the answer sheet. Read the directions carefully before you begin your test.
                    </div>
                </div>
                <div class="py-2">
                    <div class="cell-12">
                        <h5>Q22. What are the Other Test Taking Strategies?</h5>
                        Ans: Following strategies may be noted:

                        <br>
                        i)	You are provided with a few blank pages in the test paper. You can use them for your rough work. Some formulas and helping tables are given at the end of the question paper. You can take help from those for solving the questions.
                        <br>
                        ii)	Keep a very careful track of time.
                        <br>
                        iii)	Do not panic while solving your paper. Give each section a specific time period and try to complete every section on time.
                        <br>
                        iv)	If you have carefully read all the directions given in the test guide, do not waste your time by reading the directions on the answer sheet. They are exactly the same as in the guide.
                        <br>
                        v)	Do not leave any question unanswered. If you are confused or have no idea about a certain question, try making a guess for the right answer.
                        <br>
                        vi)	Do not make any extra marks on the answer sheet. Computer might register these and consider them as another marked choice.
                    </div>
